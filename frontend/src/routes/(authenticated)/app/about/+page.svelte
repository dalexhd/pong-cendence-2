		<h2 class="h2">About</h2>
		<h3> Objetivo del juego:</h3>
		El objetivo del Pong es anotar puntos golpeando una pelota con una paleta y evitando que la pelota pase por tu lado de la pantalla.

		<h3> Cómo jugar:</h3>

		Paletas:

		El juego generalmente involucra dos paletas, una a la izquierda y otra a la derecha.
		El jugador de la paleta izquierda generalmente controla su paleta con las teclas de flecha hacia arriba y hacia abajo.
		El jugador de la paleta derecha puede usar teclas adicionales o el mouse para controlar su paleta.
		<h3> Pelota:</h3>

		La pelota se encuentra en el centro de la pantalla al comienzo del juego.
		La pelota se mueve en línea recta y rebota en las paletas y en las paredes superior e inferior de la pantalla.
		<h3> Inicio del juego:</h3>

		El juego comienza con la pelota en el centro de la pantalla.
		Un jugador o el sistema (en el caso de un jugador contra la computadora) lanza la pelota.
		<h3> Jugando el juego:</h3>

		Los jugadores intentan golpear la pelota con sus paletas para que rebote y se dirija hacia el lado del oponente.
		Si un jugador no logra devolver la pelota y esta pasa por su lado de la pantalla, el oponente anota un punto.
		La pelota se reinicia en el centro después de cada punto anotado.
		<h3> Puntuación:</h3>

		La puntuación se lleva en términos de puntos acumulados.
		Cada vez que un jugador anota un punto, su oponente obtiene la oportunidad de lanzar la pelota nuevamente desde el centro.
		<h3> Ganar el juego:</h3>

		El juego continúa hasta que se alcanza un número predeterminado de puntos.
		El jugador que alcanza primero el número acordado de puntos gana la partida.
		<h3> Consejos:</h3>

		Intenta anticipar los movimientos de tu oponente para golpear la pelota de manera efectiva.
		Controla la posición de tu paleta para evitar que la pelota pase por tu lado.
		¡Ahora estás listo para disfrutar del clásico juego de Pong! ¡Buena suerte y diviértete!
